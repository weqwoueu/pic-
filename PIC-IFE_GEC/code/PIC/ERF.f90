SUBROUTINE	ERF(S, D)

IMPLICIT NONE
REAL(8), INTENT(IN)		::	S	
REAL(8)					::	B, C, D, T

B = DABS(S)

IF(B .GT. 4.) THEN
	D = 1.
ELSE
	C = DEXP(-B*B)
	T = 1. / (1 + 0.3275911 * B)
	D = 1. - (0.254829592*T - 0.284496736*T*T + 1.421413741*T*T*T - 1.453152027*T*T*T*T + 1.061405429*T*T*T*T*T) * C
ENDIF

IF(S .LT. 0.) THEN
	D = -D
ENDIF


END SUBROUTINE
